#include <SPI.h>
#include <Wire.h>
#include <Adafruit_GFX.h>
#include <Adafruit_SSD1306.h>

#define OLED_RESET 4

Adafruit_SSD1306 display(OLED_RESET);
//Adafruit_SSD1306 display(128, 64, &Wire, OLED_RESET);

void beginOled() {
  display.begin(SSD1306_SWITCHCAPVCC, 0x3C);
  display.display();
  display.clearDisplay();
  display.setTextColor(WHITE);
}

void playBlanco() {
  display.drawBitmap(32, 0, blanco, 32, 32, WHITE);
  display.drawBitmap(64, 0, blanco, 32, 32, WHITE);
}
void playOjos() {

  display.clearDisplay();
  display.drawBitmap(0, 0, ojo, 32, 32, WHITE);
  display.drawBitmap(1, 0, brillo, 32, 32, WHITE);
  display.drawBitmap(96, 0, ojo, 32, 32, WHITE);
  display.drawBitmap(97, 0, brillo, 32, 32, WHITE);
  playBlanco();
  display.display();
  delay(50);

  display.clearDisplay();
  display.drawBitmap(0, 0, ojo, 32, 32, WHITE);
  display.drawBitmap(1, 1, brillo, 32, 32, WHITE);

  display.drawBitmap(96, 0, ojo, 32, 32, WHITE);
  display.drawBitmap(97, 1, brillo, 32, 32, WHITE);
  playBlanco();
  display.display();
  delay(50);

  display.clearDisplay();
  display.drawBitmap(0, 0, ojo, 32, 32, WHITE);
  display.drawBitmap(0, 1, brillo, 32, 32, WHITE);
  display.drawBitmap(96, 0, ojo, 32, 32, WHITE);
  display.drawBitmap(96, 1, brillo, 32, 32, WHITE);
  playBlanco();
  display.display();
  delay(50);

}

